    public void modificarcliente() {
        if (idClienteSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente para modificar.");
            return;
        }
        String nombre = txtnombre.getText();
        String primerApellido = txtapellidoP.getText();
        String segundoApellido = txtapellidoM.getText();
        String direccion = txtDIreccion.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();
        String password = txtContra.getText();

        if (nombre.isEmpty() || primerApellido.isEmpty() || segundoApellido.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe completar todos los campos obligatorios.");
            return;
        }

        ClientesModel clientesModificado = new ClientesModel(
                idClienteSeleccionado,
                nombre,
                primerApellido,
                segundoApellido,
                direccion,
                telefono,
                correo,
                password
        );

        boolean exito = ClientesController.modificarcliente(clientesModificado);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Cliente modificado exitosamente.");
            cargarClientes();
        } else {
            JOptionPane.showMessageDialog(this, "Error al modificar el cliente.");
        }
    }

