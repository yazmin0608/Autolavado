    public void eliminarcliente() {
        if (idClienteSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un usuario para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea eliminar este usuario?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.NO_OPTION) {
            return;
        }
        boolean exito = ClientesController.eliminarCliente(idClienteSeleccionado);
        if (exito) {
            JOptionPane.showMessageDialog(this, "Usuario eliminado exitosamente.");
            cargarClientes(); // Recarga los datos de la tabla para que el cambio sea visible
            limpiarCampos(); // Limpia el formulario después de la eliminación
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar el usuario.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

