    public void agregarClientes() {
        String nombre = txtnombre.getText();
        String primerApellido = txtapellidoP.getText();
        String segundoApellido = txtapellidoM.getText();
        String direccion = txtDIreccion.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();
        String contra = txtContra.getText();

        if (nombre.isEmpty() || primerApellido.isEmpty() || segundoApellido.isEmpty() || direccion.isEmpty()
                || telefono.isEmpty() || correo.isEmpty() || contra.isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Favor de ingresar los datos en las cajas de texto vacias.");
            return;
        }

        ClientesModel nuevoCliente = new ClientesModel(nombre, primerApellido, segundoApellido, direccion, telefono, correo, contra);

        if (ClientesController.insertarClientes(nuevoCliente)) {
            txtnombre.setText("");
            txtapellidoP.setText("");
            txtapellidoM.setText("");
            txtDIreccion.setText("");
            txtTelefono.setText("");
            txtCorreo.setText("");
            txtContra.setText("");
            cargarClientes();
        }
    }

    public void cargarClientes() {
        modeloTabla.setRowCount(0);
        List<ClientesModel> clientes = ClientesController.mostrarTodos();
        for (ClientesModel c : clientes) {
            modeloTabla.addRow(new Object[]{c.getIdCliente(), c.getNombre(), c.getPrimerApellido(), c.getSegundoApellido(), c.getDireccion(), c.getTelefono(),
                c.getCorreo(), c.getPassword()});
        }
    }

    public void buscarcliente() {
        String terminoBusqueda = JOptionPane.showInputDialog(this, "Ingrese el nombre o apellido del cliente", "Buscar cliente", JOptionPane.QUESTION_MESSAGE);

        if (terminoBusqueda == null || terminoBusqueda.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre o apellido para buscar.");
            return;
        }

        modeloTabla.setRowCount(0);

        List<ClientesModel> clientesEncontrados = ClientesController.buscarclientesPorTermino(terminoBusqueda);

        if (!clientesEncontrados.isEmpty()) {
            for (ClientesModel c : clientesEncontrados) {
                modeloTabla.addRow(new Object[]{c.getIdCliente(), c.getNombre(), c.getPrimerApellido(), c.getSegundoApellido(),
                    c.getDireccion(), c.getTelefono(), c.getCorreo(), c.getPassword()});
            }
            JOptionPane.showMessageDialog(this, clientesEncontrados.size() + " cliente/s encontrado/s.");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron clientes con ese nombre o apellido.");
        }
    }

