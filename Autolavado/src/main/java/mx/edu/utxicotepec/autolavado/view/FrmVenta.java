    private void eliminarVenta() {
        int filaSeleccionada = tblsv.getSelectedRow();

        // Validar que se haya seleccionado una fila
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una venta de la tabla para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtener el ID de la venta desde la primera columna de la tabla
        int idVentaAEliminar = (int) modeloTabla.getValueAt(filaSeleccionada, 0);

        // Confirmar la acción con el usuario
        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea eliminar la venta seleccionada?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Llamar al método del controlador para eliminar la venta
            if (VentaServicioController.eliminarVenta(idVentaAEliminar)) {
                JOptionPane.showMessageDialog(this, "Venta eliminada con éxito.");
                limpiarCampos();
                cargarVenta(); // Refrescar la tabla después de la eliminación
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar la venta.");
            }
        }
    }
