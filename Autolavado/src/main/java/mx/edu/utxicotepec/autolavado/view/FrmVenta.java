    public void agregarservicio() {
        if (combocajero.getSelectedItem() == null || combolavador.getSelectedItem() == null || comboservicio.getSelectedItem() == null || comboesattus.getSelectedItem() == null || combopago.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una opción en todos los menús desplegables.", "Error validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Validar que los campos de texto no estén vacíos
        String vehiculoTexto = txtvehiculo.getText();

        if (vehiculoTexto.isEmpty()) { // Add validation for the vehicle text field
            JOptionPane.showMessageDialog(this, "Favor de ingresar los datos en las cajas de texto vacías.", "Error validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idVehiculo = VehiculoController.buscarIdVehiculoPorMatricula(vehiculoTexto);

        if (idVehiculo == 0) {
            JOptionPane.showMessageDialog(this, "El vehículo no existe en la base de datos. Por favor, regístrelo primero.", "Error de vehículo", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Obtener los objetos y valores del formulario
        UsuariosModel cajeroSeleccionado = (UsuariosModel) combocajero.getSelectedItem();
        ServiciosModel servicioSeleccionado = (ServiciosModel) comboservicio.getSelectedItem();
        UsuariosModel lavadorSeleccionado = (UsuariosModel) combolavador.getSelectedItem();

        VentaServicioModel nuevaVenta = new VentaServicioModel();

        // Asignar IDs y otros valores.
        nuevaVenta.setIdUsuarioC(cajeroSeleccionado.getIdUsuario()); // Get ID from the JComboBox
        nuevaVenta.setIdUsuarioO(lavadorSeleccionado.getIdUsuario());
        nuevaVenta.setIdServicio(servicioSeleccionado.getIdServiicios());
        nuevaVenta.setIdVehiculo(idVehiculo);

        // NOTA: La tabla `tbc_venta_servicios` tiene un `idVehiculo`. Debes obtener
        // este valor del formulario. El valor `0` es solo un marcador.
        // 5. Formatear y asignar la fecha y hora
        String fecha = txtaño.getText() + "-" + txtmes.getText() + "-" + txtdia.getText();
        String hora = txthora.getText() + ":" + txtminutos.getText() + ":00";

        nuevaVenta.setFecha(fecha);
        nuevaVenta.setHora(hora);

        // 6. Asignar los valores de los ComboBox de Estatus y Pago
        nuevaVenta.setEstatus((String) comboesattus.getSelectedItem());
        nuevaVenta.setPagado((String) combopago.getSelectedItem());

        // 7. Insertar la venta en la base de datos
        if (VentaServicioController.insertarVentaS(nuevaVenta)) {
            JOptionPane.showMessageDialog(this, "Venta registrada con éxito.");
            limpiarCampos(); // Se recomienda llamar a este método para no repetir código
            cargarVenta();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar la venta.");
        }
    }

    public void cargarVenta() {
        modeloTabla.setRowCount(0);
        List<VentaServicioModel> ventas = VentaServicioController.mostrarTodos();
        for (VentaServicioModel v : ventas) {
            modeloTabla.addRow(new Object[]{
                v.getIdVentaServicio(),
                v.getIdUsuarioC(),
                v.getIdUsuarioO(),
                v.getIdServicio(),
                v.getIdVehiculo(),
                v.getFecha(),
                v.getHora(),
                v.getEstatus(),
                v.getPagado()
            });
        }
    }

