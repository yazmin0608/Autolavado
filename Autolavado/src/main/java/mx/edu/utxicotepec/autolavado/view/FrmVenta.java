    public void buscarVentas() {
        String terminoBusqueda = JOptionPane.showInputDialog(this, "Ingrese la matrícula, nombre del cajero o lavador para buscar", "Buscar Ventas", JOptionPane.QUESTION_MESSAGE);

        if (terminoBusqueda == null || terminoBusqueda.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un término para buscar.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Llamada al nuevo método en el controlador que busca por un solo término
        List<VentaServicioModel> resultados = VentaServicioController.buscarVentasPorTermino(terminoBusqueda);

        // Limpiar la tabla y mostrar los resultados
        modeloTabla.setRowCount(0);
        if (!resultados.isEmpty()) {
            for (VentaServicioModel v : resultados) {
                modeloTabla.addRow(new Object[]{
                    v.getIdVentaServicio(),
                    v.getIdUsuarioC(),
                    v.getIdUsuarioO(),
                    v.getIdServicio(),
                    v.getIdVehiculo(),
                    v.getFecha(),
                    v.getHora(),
                    v.getEstatus(),
                    v.getPagado()
                });
            }
            JOptionPane.showMessageDialog(this, resultados.size() + " Venta(s) encontrada(s).");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron ventas con esos criterios.");
        }
    }

