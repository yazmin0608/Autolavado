    private void modificarVenta() {
        // 1. Validaciones iniciales
        if (idVentaAModificar == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una venta para modificar.");
            return;
        }

        if (combocajero.getSelectedItem() == null || combolavador.getSelectedItem() == null || comboservicio.getSelectedItem() == null || comboesattus.getSelectedItem() == null || combopago.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una opción en todos los menús desplegables.", "Error validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String vehiculoTexto = txtvehiculo.getText();
        String año = txtaño.getText();
        String mes = txtmes.getText();
        String dia = txtdia.getText();
        String hora = txthora.getText();
        String minutos = txtminutos.getText();

        if (vehiculoTexto.isEmpty() || año.isEmpty() || mes.isEmpty() || dia.isEmpty() || hora.isEmpty() || minutos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Favor de ingresar los datos en todas las cajas de texto.", "Error validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Obtener los valores del formulario
        int idCajero = ((UsuariosModel) combocajero.getSelectedItem()).getIdUsuario();
        int idLavador = ((UsuariosModel) combolavador.getSelectedItem()).getIdUsuario();
        int idServicio = ((ServiciosModel) comboservicio.getSelectedItem()).getIdServiicios();

        int idVehiculo = VehiculoController.buscarIdVehiculoPorMatricula(vehiculoTexto);

        if (idVehiculo == 0) {
            JOptionPane.showMessageDialog(this, "El vehículo no existe en la base de datos.", "Error de vehículo", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String fecha = año + "-" + mes + "-" + dia;
        String horaCompleta = hora + ":" + minutos + ":00";

        // 3. Crear el objeto VentaServicioModel con el ID del registro seleccionado
        VentaServicioModel ventaModificada = new VentaServicioModel();
        ventaModificada.setIdVentaServicio(idVentaAModificar);
        ventaModificada.setIdUsuarioC(idCajero);
        ventaModificada.setIdUsuarioO(idLavador);
        ventaModificada.setIdServicio(idServicio);
        ventaModificada.setIdVehiculo(idVehiculo);
        ventaModificada.setFecha(fecha);
        ventaModificada.setHora(horaCompleta);
        ventaModificada.setEstatus((String) comboesattus.getSelectedItem());
        ventaModificada.setPagado((String) combopago.getSelectedItem());

        // 4. Llamar al método del controlador para actualizar la base de datos
        if (VentaServicioController.actualizarVenta(ventaModificada)) {
            JOptionPane.showMessageDialog(this, "Venta modificada con éxito.");
            idVentaAModificar = -1; // Resetear la variable de control
            limpiarCampos();
            cargarVenta(); // Refrescar la tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al modificar la venta.");
        }
    }

